//Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma,
// subtração,multiplicação e divisão desses valores.

function somas(n1, n2){
   console.log(`A soma é ${n1 + n2}`);
   console.log(`A subtração é ${n1 - n2}`);
   console.log(`A multiplicação é ${n1 * n2}`);
   console.log(`A divisão é ${n1 / n2}`);
}

somas(50, 10)